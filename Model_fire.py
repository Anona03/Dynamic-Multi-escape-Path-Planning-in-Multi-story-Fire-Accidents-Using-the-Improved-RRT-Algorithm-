# 设置火源的起始位置和扩散速度
fire_start = [(5, 5), (15, 21)]  # 火源起始位置 (行, 列)
fire_speed = 1  # 每次扩散的步数

# 创建画布和子图
fig, ax = plt.subplots(figsize=(8, 6))

# 初始化火源图
fire_map = np.zeros_like(floor_plan, dtype=float)

for (fr, fc) in fire_start:
    if 0 <= fr < floor_plan.shape[0] and 0 <= fc < floor_plan.shape[1]:
        fire_map[fr, fc] = 1


def spread_fire():
    # 在每个步骤扩散火源
    global fire_map
    new_fire_map = fire_map.copy()

    # 扩散到上下左右的邻居
    for i in range(fire_map.shape[0]):
        for j in range(fire_map.shape[1]):
            if fire_map[i, j] == 1:
                # 扩散到邻近的格子
                if i > 0 and floor_plan[i - 1, j] == 0: new_fire_map[i - 1, j] = 1
                if i < fire_map.shape[0] - 1 and floor_plan[i + 1, j] == 0: new_fire_map[i + 1, j] = 1
                if j > 0 and floor_plan[i, j - 1] == 0: new_fire_map[i, j - 1] = 1
                if j < fire_map.shape[1] - 1 and floor_plan[i, j + 1] == 0: new_fire_map[i, j + 1] = 1

    fire_map = new_fire_map


# 动态显示火源扩散过程
for _ in range(10):  # 扩散20步
    # 更新火源状态
    spread_fire()

    # 显示栅格图
    ax.imshow(floor_plan + fire_map, cmap='binary', interpolation='nearest')

    # 添加网格线（可选）
    ax.grid(which='major', axis='both', linestyle='-', color='gray', linewidth=0.3, alpha=0.5)
    ax.set_xticks(np.arange(-0.5, 30, 1))
